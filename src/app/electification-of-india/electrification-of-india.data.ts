export const railwayElectrificationYearWiseData = railwayElectrificationData.slice(10).flatMap(summary => {
  const startingYear = summary.from;
  const currentYear = summary.to;
  const timePeriodInYears = currentYear - startingYear;

  if (timePeriodInYears === 1) {
    return summary;
  }

  const equalDistribution = Math.round(summary.electrification / timePeriodInYears);
  const fillInTheBlanks = Array(timePeriodInYears).fill(null).map((noop, i) => {
    const year = startingYear + i + 1;
    return {
      'electrification': equalDistribution,
      'from': year - 1,
      'to': year
    };
  })

  return fillInTheBlanks;
});


