import { rgbColor, scaleNumber, shadeColor } from '../utils/functions';

type RegistrationsByState = { name: string; counts: number[]; pcChange: number[]; absChange: number[]; colors: string[]; };

const calculateYearlyIncrements = ([name, counts]: [string, number[]]): RegistrationsByState => {
    const pcChange = counts.map((count, i) => i && count && counts[i - 1] ? 100 * (count - counts[i - 1]) / counts[i - 1] : 0).slice(1);
    const absChange = counts.map((count, i) => i && count && counts[i - 1] ? (count - counts[i - 1]) : 0).slice(1);
    const colors = pcChange.map(pc => {
        if (pc === 0) {
            return null;
        }
        // let scaledPc = Math.log10(Math.abs(pc) + 10);
        // scaledPc = scaleNumber(scaledPc, 250, 0, 0, 2);
        const scaledPc = scaleNumber(Math.abs(pc), 250, 0, 0, 200);
        let colorOverflow = Math.abs(pc) - 100;
        colorOverflow = 0 // colorOverflow > 0 ? colorOverflow : 0
        return pc > 0 ? rgbColor(255 - colorOverflow, scaledPc, scaledPc) : rgbColor(scaledPc, 255 - colorOverflow, scaledPc);
    });
    return { name, counts: counts.slice(1), pcChange, absChange, colors };
}

